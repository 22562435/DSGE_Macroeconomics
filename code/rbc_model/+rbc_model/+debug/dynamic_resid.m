function [lhs, rhs] = dynamic_resid(y, x, params, steady_state)
T = NaN(3, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = y(12)^params(2);
T(2) = y(16)*T(1);
T(3) = y(15)^(1-params(2));
lhs(1) = 1/y(9);
rhs(1) = params(1)*1/y(17)*(1+y(21)-params(3));
lhs(2) = 1/y(9)*y(14);
rhs(2) = params(5)/(1-y(15));
lhs(3) = y(9)+y(10);
rhs(3) = y(11);
lhs(4) = y(13);
rhs(4) = y(11)*params(2)/y(12);
lhs(5) = y(14);
rhs(5) = y(11)*(1-params(2))/y(15);
lhs(6) = y(10);
rhs(6) = y(12)-(1-params(3))*y(4);
lhs(7) = y(11);
rhs(7) = T(2)*T(3);
lhs(8) = log(y(16));
rhs(8) = params(4)*log(y(8))+x(1);
end
