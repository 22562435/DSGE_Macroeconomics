function [lhs, rhs] = static_resid(y, x, params)
T = NaN(4, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = 1/y(1);
T(2) = y(4)^params(2);
T(3) = y(8)*T(2);
T(4) = y(7)^(1-params(2));
lhs(1) = T(1);
rhs(1) = T(1)*params(1)*(1+y(5)-params(3));
lhs(2) = T(1)*y(6);
rhs(2) = params(5)/(1-y(7));
lhs(3) = y(1)+y(2);
rhs(3) = y(3);
lhs(4) = y(5);
rhs(4) = y(3)*params(2)/y(4);
lhs(5) = y(6);
rhs(5) = y(3)*(1-params(2))/y(7);
lhs(6) = y(2);
rhs(6) = y(4)-y(4)*(1-params(3));
lhs(7) = y(3);
rhs(7) = T(3)*T(4);
lhs(8) = log(y(8));
rhs(8) = log(y(8))*params(4)+x(1);
end
